      docMetrees.text(`Total Suppl√©ments (${suppZoneName}): ${suppSubtotal.toFixed(2)} ML`, 170, finalY2 + 4, { align: 'right' });
      
      tableStartY = finalY2 + 10;
    });
  }
  
