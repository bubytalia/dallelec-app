          1: { cellWidth: 35 },
          2: { cellWidth: 15 },
          3: { cellWidth: 35 },
          4: { cellWidth: 10 },
          5: { cellWidth: 15 },
          6: { cellWidth: 15 }
        },

      });
      
      // Totale per la zona supplementi (esattamente come DevisPdf)
